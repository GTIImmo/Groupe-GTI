create table if not exists public.app_dossier_detail_current (
    app_dossier_id bigint primary key,
    hektor_annonce_id bigint not null,
    source_updated_at text,
    source_hash text not null,
    detail_payload_json text not null,
    refreshed_at timestamptz not null default now()
);

create table if not exists public.app_work_item_current (
    id bigint generated by default as identity primary key,
    app_dossier_id bigint not null,
    hektor_annonce_id bigint not null,
    archive text,
    numero_dossier text,
    numero_mandat text,
    titre_bien text not null,
    commercial_nom text,
    type_demande_label text,
    work_status text,
    internal_status text,
    priority text,
    validation_diffusion_state text,
    etat_visibilite text,
    motif_blocage text,
    has_open_blocker boolean not null default false,
    next_action text,
    date_relance_prevue date,
    date_entree_file timestamptz,
    date_derniere_action timestamptz,
    age_jours integer,
    source_hash text not null,
    refreshed_at timestamptz not null default now(),
    unique (app_dossier_id, type_demande_label, date_entree_file)
);

create table if not exists public.app_filter_catalog_current_store (
    id bigint generated by default as identity primary key,
    filter_type text not null,
    filter_value text not null,
    sort_order integer not null default 0,
    source_hash text not null,
    refreshed_at timestamptz not null default now(),
    unique (filter_type, filter_value)
);

create table if not exists public.app_delta_run (
    id uuid primary key default gen_random_uuid(),
    mode text not null default 'upgrade',
    status text not null default 'running',
    scope text not null default 'annonces_current',
    started_at timestamptz not null default now(),
    finished_at timestamptz,
    dossiers_detected integer not null default 0,
    dossiers_upserted integer not null default 0,
    details_upserted integer not null default 0,
    work_items_replaced integer not null default 0,
    filters_replaced integer not null default 0,
    deleted_dossiers integer not null default 0,
    notes jsonb not null default '{}'::jsonb
);

create index if not exists idx_app_dossier_current_annonce on public.app_dossier_current (hektor_annonce_id);
create index if not exists idx_app_dossier_current_statut on public.app_dossier_current (statut_annonce, archive, diffusable);
create index if not exists idx_app_dossier_current_commercial on public.app_dossier_current (commercial_id, commercial_nom);
create index if not exists idx_app_dossier_detail_current_annonce on public.app_dossier_detail_current (hektor_annonce_id);
create index if not exists idx_app_work_item_current_status on public.app_work_item_current (work_status, internal_status, priority);
create index if not exists idx_app_work_item_current_annonce on public.app_work_item_current (hektor_annonce_id);
create index if not exists idx_app_filter_catalog_current_type on public.app_filter_catalog_current_store (filter_type, sort_order, filter_value);
create index if not exists idx_app_delta_run_started_at on public.app_delta_run (started_at desc, status);

create or replace view public.app_dashboard_v1
with (security_invoker=on) as
select
    null::uuid as sync_run_id,
    (select count(*) from public.app_dossiers_current) as total_dossiers,
    (select count(*) from public.app_work_items_current) as total_demandes,
    (select count(*) from public.app_dossiers_current where coalesce(numero_mandat, '') = '') as total_sans_mandat,
    (select count(*) from public.app_dossiers_current where coalesce(has_open_blocker, false) = true) as total_bloques,
    (select count(*) from public.app_dossiers_current where coalesce(validation_diffusion_state, '') = 'valide') as total_valides_diffusion,
    (select count(*) from public.app_dossiers_current where coalesce(etat_visibilite, '') = 'visible') as total_visibles;

