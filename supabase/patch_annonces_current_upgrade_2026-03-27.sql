create table if not exists public.app_dossier_current (
    app_dossier_id bigint primary key,
    hektor_annonce_id bigint not null,
    archive text,
    diffusable text,
    nb_portails_actifs integer,
    has_diffusion_error boolean,
    portails_resume text,
    offre_id text,
    offre_state text,
    offre_last_proposition_type text,
    compromis_id text,
    compromis_state text,
    vente_id text,
    numero_dossier text,
    numero_mandat text,
    titre_bien text not null,
    ville text,
    type_bien text,
    prix numeric,
    commercial_id text,
    commercial_nom text,
    agence_nom text,
    statut_annonce text,
    validation_diffusion_state text,
    etat_visibilite text,
    alerte_principale text,
    priority text,
    has_open_blocker boolean not null default false,
    commentaire_resume text,
    date_relance_prevue date,
    dernier_event_type text,
    dernier_work_status text,
    source_updated_at text,
    source_hash text not null,
    refreshed_at timestamptz not null default now()
);

